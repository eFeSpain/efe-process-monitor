// uiLang tracks the last UI language resolved from a request, so background
// goroutines (the live monitor) emit desktop notifications in the language the
// operator has selected — they have no per-request context of their own.
var (
	uiLangMu sync.RWMutex
	uiLang   = "es"
)

func currentLang() string {
	uiLangMu.RLock()
	defer uiLangMu.RUnlock()
	return uiLang
}

// langFrom resolves the UI language from the ?lang query or the lang cookie and
// records it as the current language for background notifications.
func langFrom(r *http.Request) string {
	lang := "es"
	if l := r.URL.Query().Get("lang"); l != "" {
		if _, ok := translations[l]; ok {
			lang = l
		}
	} else if c, err := r.Cookie("lang"); err == nil {
		if _, ok := translations[c.Value]; ok {
			lang = c.Value
		}
	}
	uiLangMu.Lock()
	uiLang = lang
	uiLangMu.Unlock()
	return lang
}

func strings_(lang string) map[string]string {
	if t, ok := translations[lang]; ok {
		return t
	}
	return translations["es"]
}
